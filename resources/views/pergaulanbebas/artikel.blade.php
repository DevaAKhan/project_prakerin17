  BAB III. PENUTUP 

<br>

<br>

A. KESIMPULAN 

<br> <br>


  Pergaulan dibagi menjadi dua yaitu pergaulan yang mengarah pada hal-hal yang positif dan pergaulan negatif atau pergaulan bebas. Pergaulan bebas adalah pergaulan yang tidak memiliki batasannya, sehinga remaja yang terlibat dalam pergaulan bebas akan melakukan tindakan diluar batas. 
  Pergaulan kearah yang positif dapat memberikan kontribusi yang baik kepada diri para remaja dan masyarakat di sekitarnya, seperti menambah pergaulan, wawasan dan informasi serta pengetahuan yang bermanfaat bagi dirinya dimasa depan.  

<br> <br>

B. SARAN <br> <br>

Tidak semua remaja memahami tentang berbagai tindakan yang mereka lakukan (merugikan atau menguntungkan), untuk itu bagi para remaja agar lebih berhati-hati terhadap pergaulan diluar sana serta lebih mengoptimalkan dan meningkatkan kegiatan-kegiatan hal yang lebih positif. <br> <br>




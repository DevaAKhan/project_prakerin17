BaB 1. PEMBUKAAN <br>

<br>

Sebagai remaja kita harus memiliki sikap dan kepribadian yang baik. Seperti bertutur kata yang sopan dan mempunyai pemikiran yang bergolong hal-hal yang positif, sehingga dengan begitu tertanamnya hal baik pada diri itu sendiri. Oleh sebab itu kita sebagai remaja harus bisa memilih teman yang  baik agar tidak terjerumus dalam hal yang negatif. Bukan hanya remaja itu sendiri begitu pula peran orang tua, karena peran orang tua sangat penting dalam kehidupan anaknya yang mulai memasuki usia remaja hingga dewasa. Maka dari itu orang tua harus memberikan bimbingan, perhatian dan kasih sayang yang cukup serta memberi contoh yang baik. Agar anaknya tidak terjerumus pada pergaulan bebas.  
Karena pergaulan bebas dimulai sejak usia remaja beranjak dewasa. Sehinga terjadinya perubahan pada kepribadian seorang remaja. Perubahan itu terjadi tergantung pada remaja itu sendiri. Lingkungan juga sangat berpengaruh pada pergaulan bebas. Apabila lingkungan itu buruk maka akan semakin lebih mudah seseorang terjerumus pada pergaulan bebas tergantung pada lingkungannya.  Adapun salah satu contoh dari pergaulan bebas yaitu penggunaan obat-obatan yang terlarang,seperti narkoba. Dibawah ini akan menjelaskan mengenai ciri-ciri, penyebab, dampak akibat pergaulan bebas pada usia dini.

<br> <br>

BAB II. PEMBAHASAN 

<br>



<br>

1.	Pengertian pergaulan bebas 

<br> <br>



pergaulan bebas adalah salah satu bentuk perilaku menyimpang yang melewati batas dari kewajiban, aturan, tuntutan, syarat, dan perasaan malu. 
Pergaulan bebas juga dapat diartikan sebagai perilaku menyimpang yang melanggar norma agama maupun norma kesusilaan. 
Pergaulan bebas merupakan sebuah perilaku negatif sebagai ekspresi penolakan remaja. 
Beberapa penyebab remaja melakukan pergaulan bebas yaitu kegagalan remaja menyerap norma-norma agama dan norma-norma pancasila, sikap mental yang tidak sehat, pelampiasan rasa kecewa terhadap keluarga yang tidak harmonis, dan lain sebagainya. 
Pergaulan memiliki pengaruh yang besar terhadap proses pembentukan kepribadian. Remaja sangat rentan terpengaruh oleh pergaulan yang ada pada lingkungannya. 
Remaja harus cerdas untuk menghindari pergaulan bebas, karena seumuran remaja masih mudah goyah dan sedang mencari jati diri.<br> <br>


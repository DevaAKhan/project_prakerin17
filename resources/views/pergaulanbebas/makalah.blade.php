B. PENGERTIAN REMAJA <br> <br> 



Menurut psikologi, remaja adalah suatu periode transisi dari masa awal anak anak hingga masa awal dewasa, yang dimasuki pada usia kira kira 10 hingga 12 tahun dan berakhir pada usia 18 tahun hingga 22 tahun.[butuh rujukan] Masa remaja bermula pada perubahan fisik yang cepat, pertambahan berat dan tinggi badan yang dramatis, perubahan bentuk tubuh, dan perkembangan karakteristik seksual seperti pembesaran buah dada, perkembangan pinggang dan kumis, dan dalamnya suara. Pada perkembangan ini, pencapaian kemandirian dan identitas sangat menonjol (pemikiran semakin logis, abstrak, dan idealistis) dan semakin banyak menghabiskan waktu di luar keluarga. 
Dilihat dari bahasa inggris "teenager", remaja artinya yakni manusia berusia belasan tahun.Dimana usia tersebut merupakan perkembangan untuk menjadi dewasa. Oleh sebab itu orang tua dan pendidik sebagai bagian masyarakat yang lebih berpengalaman memiliki peranan penting dalam membantu perkembangan remaja menuju kedewasaan.[butuh rujukan] Remaja juga berasal dari kata latin "adolensence" yang berarti tumbuh atau tumbuh menjadi dewasa. Istilah adolensence mempunyai arti yang lebih luas lagi yang mencakup kematangan mental, emosional, sosial, dan fisik (Hurlock, 1992). Remaja memiliki tempat di antara anak-anak dan orang tua karena sudah tidak termasuk golongan anak tetapi belum juga berada dalam golongan dewasa atau tua. Seperti yang dikemukakan oleh Calon (dalam Monks, dkk 1994) bahwa masa remaja menunjukkan dengan jelas sifat transisi atau peralihan karena remaja belum memperoleh status dewasa dan tidak lagi memiliki status anak. Menurut Sri Rumini & Siti Sundari (2004: 53) masa remaja adalah peralihan dari masa anak dengan masa dewasa yang mengalami perkembangan semua aspek / fungsi untuk memasuki masa dewasa.Masa remaja berlangsung antara umur 12 tahun sampai dengan 21 tahun bagi wanita dan 13 tahun sampai dengan 22 tahun bagi pria. Sedangkan menurut Zakiah Darajat (1990: 23) remaja adalah: Masa peralihan di antara masa kanak-kanak dan dewasa. Dalam masa ini anak mengalami masa pertumbuhan dan masa perkembangan fisiknya maupun perkembangan psikisnya. Mereka bukanlah anak-anak baik bentuk badan ataupun cara berfikir atau bertindak, tetapi bukan pula orang dewasa yang telah matang. Hal senada diungkapkan oleh Santrock (2003: 26) bahwa remaja (adolescene) diartikan sebagai masa perkembangan transisi antara masa anak dan masa dewasa yang mencakup perubahan biologis, kognitif, dan sosialemosional. Batasan usia remaja yang umum digunakan oleh para ahli adalah antara 12 hingga 21 tahun. 

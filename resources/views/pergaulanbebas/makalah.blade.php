C.	PENGERTIAN PERGAULAN BEBAS  <br> <br>



  Pergaulan bebas adalah salah satu kebutuhan hidup dari makhluk manusia sebab manusia adalah makhluk sosial yang dalam kesehariannya membutuhkan orang lain, dan hubungan antar manusia dibina melalui suatu pergaulan (interpersonal relationship).Pergaulan juga adalah HAM setiap individu dan itu harus dibebaskan, sehingga setiap manusia tidak boleh dibatasi dalam pergaulan, apalagi dengan melakukan diskriminasi, sebab hal itu melanggar HAM. Jadi pergaulan antar manusia harusnya bebas, tetapi tetap mematuhi norma hukum, norma agama, norma budaya, serta norma bermasyarakat. Jadi, kalau secara medis kalau pergaulan bebas namun teratur atau terbatasi aturan-aturan dan norma-norma hidup manusia tentunya tidak akan menimbulkan ekses-ekses seperti saat ini. Pergaulan bebas juga dapat didefinisikan sebagai melencengnya pergaulan seseorang dari pergaulan yang benar , pergaulan bebas diidentikan sebagai bentuk dari pergaulan luar batas atau bisa juga disebut pergaulan liar. <br>
  Pergaulan bebas menurut agama 
Dilihat dari segi katanya dapat ditafsirkan dan dimengerti apa maksud dari istilah pergaulan bebas. Dari segi bahasa pergaulan artinya proses bergaul, sedangkan bebas artinya terlepas dari ikatan. Jadi pergaulan bebas artinya proses bergaul dengan orang lain terlepas dari ikatan yang mengatur pergaulan. Islam telah mengatur bagaimana cara bergaul dengan lawan jenis. Hal ini telah tercantum dalam surat An-Nur ayat 30-31. Telah dijelaskan bahwa hendaknya kita menjaga pandangan mata dalam bergaul. Lalu bagaiamana hal yang terjadi dalam pergaulan bebas? Tentunya banyak hal yang bertolak belakang dengan aturan-aturan yang telah Allah tetapkan dalam etika pergaulan. Karena dalam pergaulan bebas itu tidak dapat menjamin kesucian seseorang. 
